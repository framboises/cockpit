// Fonction pour r√©cup√©rer et afficher les horaires de lever et coucher du soleil
function fetchSunTimes() {
    fetch('/sun_times')
        .then(response => response.json())
        .then(data => {
            let leverTime = new Date(data.lever);
            let coucherTime = new Date(data.coucher);

            // D√©terminer l'ordre d'affichage en fonction des horaires
            // Par d√©faut, on affiche le lever (‚òÄÔ∏è) puis le coucher (üåô)
            let firstIcon = "‚òÄÔ∏è", firstTime = leverTime;
            let secondIcon = "üåô", secondTime = coucherTime;
            
            // Si le lever arrive apr√®s le coucher (cas de nuit par exemple),
            // alors on inverse l'ordre pour afficher d'abord l'√©v√©nement le plus proche.
            if (leverTime > coucherTime) {
                firstIcon = "üåô";
                firstTime = coucherTime;
                secondIcon = "‚òÄÔ∏è";
                secondTime = leverTime;
            }

            let meteoPrevisionsDiv = document.getElementById('meteo-previsions');

            // V√©rifier si le bloc existe d√©j√† pour √©viter les doublons
            if (document.getElementById('sun-times-block')) return;

            // Cr√©er le bloc unique pour l'affichage du lever et coucher du soleil,
            // en utilisant les ic√¥nes dynamiques.
            let sunTimesDiv = document.createElement('div');
            sunTimesDiv.id = 'sun-times-block';
            sunTimesDiv.className = 'meteo-day sun-block';
            sunTimesDiv.innerHTML = `
                <div class="sun-item"><span class="sun-icon">${firstIcon}</span> ${formatHour(firstTime)}</div>
                <div class="sun-item"><span class="sun-icon">${secondIcon}</span> ${formatHour(secondTime)}</div>
            `;

            // Ins√©rer ce bloc en premier dans le conteneur m√©t√©o
            meteoPrevisionsDiv.prepend(sunTimesDiv);
        })
        .catch(error => console.error('Erreur lors de la r√©cup√©ration des horaires du soleil :', error));
}

// Fonction pour formater l'heure en "HH:MM"
function formatHour(dateTimeStr) {
    let date = new Date(dateTimeStr);
    return date.toLocaleTimeString('fr-FR', { hour: '2-digit', minute: '2-digit' });
}

